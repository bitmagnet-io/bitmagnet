package jen

import (
	"github.com/bitmagnet-io/bitmagnet/internal/wasm/gen/spec"
	"github.com/dave/jennifer/jen"
	"google.golang.org/protobuf/reflect/protoreflect"
)

const (
	pkgWazero       = "github.com/tetratelabs/wazero"
	pkgWazeroAPI    = pkgWazero + "/api"
	pkgContext      = "context"
	pkgWasm         = "github.com/bitmagnet-io/bitmagnet/internal/wasm"
	pkgProtoImpl    = "google.golang.org/protobuf/runtime/protoimpl"
	pkgProtoReflect = "google.golang.org/protobuf/reflect/protoreflect"
)

func newFile(s spec.FileInfo) *jen.File {
	return jen.NewFilePathName(string(s.GoImportPath), string(s.GoPackageName))
}

func genHeader(f *jen.File, s spec.FileInfo) {
	genStandaloneComments(f, s.SyntaxCommentLocation)
	genGeneratedHeader(f, s)
	genStandaloneComments(f, s.PackageCommentLocation)
}

func genStandaloneComments(f *jen.File, loc protoreflect.SourceLocation) {
	for _, c := range loc.LeadingDetachedComments {
		f.HeaderComment(c)
	}

	if loc.LeadingComments != "" {
		f.HeaderComment(loc.LeadingComments)
	}
}

func genGeneratedHeader(f *jen.File, s spec.FileInfo) {
	f.HeaderComment("Code generated by protoc-gen-go-plugin. DO NOT EDIT.")
	f.HeaderComment("protoc version: " + s.ProtocVersion)
	f.HeaderComment("source: " + s.Path)

	if s.Deprecated {
		f.HeaderComment("Deprecated")
	}
}
