package logging

import (
	"github.com/adrg/xdg"
	"go.uber.org/zap/zapcore"
	"path"
	"strings"
	"time"
)

type Config struct {
	Level       string
	Development bool
	Json        bool
	FileRotator FileRotatorConfig
}

type FileRotatorConfig struct {
	Enabled    bool
	Level      string
	Path       string
	BaseName   string
	MaxAge     time.Duration
	MaxSize    int
	MaxBackups int
	BufferSize int
}

func NewDefaultConfig() Config {
	return Config{
		Level:       "info",
		Development: false,
		Json:        false,
		FileRotator: FileRotatorConfig{
			Enabled:    false,
			Level:      "debug",
			Path:       path.Join(xdg.DataHome, "bitmagnet", "logs"),
			BaseName:   "bitmagnet",
			MaxAge:     time.Minute * 60,
			MaxSize:    1_000_000 * 100,
			BufferSize: 1_000,
			MaxBackups: 5,
		},
	}
}

